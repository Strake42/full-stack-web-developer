// JavaScript Data Types

// 1. Number
let age = 25;
console.log("Number:", age); // Output: Number: 25

// 2. String
let name = "John Doe";
console.log("String:", name); // Output: String: John Doe

// 3. Boolean
let isStudent = true;
console.log("Boolean:", isStudent); // Output: Boolean: true

// 4. Undefined
let undefinedVariable;
console.log("Undefined:", undefinedVariable); // Output: Undefined: undefined

// 5. Null
let nullVariable = null;
console.log("Null:", nullVariable); // Output: Null: null

// 6. Object
let person = {
    firstName: "Jane",
    lastName: "Doe",
    age: 30
};
console.log("Object:", person); // Output: Object: { firstName: 'Jane', lastName: 'Doe', age: 30 }

// 7. Array (which is a type of object)
let colors = ["red", "green", "blue"];
console.log("Array:", colors); // Output: Array: [ 'red', 'green', 'blue' ]

// 8. Function
function greet() {
    return "Hello, World!";
}
console.log("Function:", greet()); // Output: Function: Hello, World!

// Differences:
// - Number: Represents both integer and floating-point numbers.
// - String: Represents a sequence of characters.
// - Boolean: Represents true or false values.
// - Undefined: A variable that has been declared but not assigned a value.
// - Null: Represents the intentional absence of any object value.
// - Object: A collection of properties, where each property is a key-value pair.
// - Array: A special type of object used to store ordered collections of values.
// - Function: A block of code designed to perform a particular task.